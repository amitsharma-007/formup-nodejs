module.exports = (sequelize, DataTypes) => {
  const Scheme = sequelize.define("scheme", {
    name: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    email: {
      type: DataTypes.STRING,
      allowNull: false,
      unique: true,
    },
    mobile: {
      type: DataTypes.STRING,
      allowNull: false,
      unique: true,
    },
  });
  return Scheme;
};
